package MVC.Model.DungeonItems.Items;

import MVC.Model.DungeonAdventure.DungeonCharacters.DungeonCharacter;
import MVC.Model.DungeonAdventure.DungeonCharacters.EntityFactory;
import MVC.Model.Physics.Vec2;

public class Bomb extends Item
{
    private final EntityFactory myEntityFactory;

    private long myCurrentFrame;

    /**
     * Bomb constructor that calls its parent constructor to initialize its type and location.
     */
    public Bomb(final EntityFactory theEntityFactory) {
        super("Bomb", new Vec2(), theEntityFactory);
        myEntityFactory = theEntityFactory;
        myCurrentFrame = 0;
    }

    /**
     * Bomb overloaded constructor that calls its parent constructor to initialize its type and location.
     * @param thePosition The Bomb's location.
     */
    public Bomb(final Vec2 thePosition, final EntityFactory theEntityFactory) {
        super("Bomb", thePosition, theEntityFactory);
        myEntityFactory = theEntityFactory;
        myCurrentFrame = 0;
    }

    /**
     * This methods serves as the blast generated by a Bomb detonating when a DungeonCharacter enters a Room.
     * It reduces theCharacter's hit points by half.
     * @param theCharacter The DungeonCharacter whose hit points will be affected.
     */
    public void itemBehavior(final DungeonCharacter theCharacter)
    {
        theCharacter.setHitPoints(theCharacter.getHitPoints() / 2);
    }

    @Override
    public void update()
    {
        //itemBehavior();
        myCurrentFrame++;
    }
}
